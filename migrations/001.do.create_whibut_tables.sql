CREATE TABLE whibut_users (
  id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY, 
  username TEXT NOT NULL UNIQUE,
  password TEXT NOT NULL,
  date_created TIMESTAMP DEFAULT now() NOT NULL,
  date_modified TIMESTAMP
);

CREATE TABLE whibut_movies (
  id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  user_id INTEGER REFERENCES whibut_users(id) ON DELETE CASCADE NOT NULL,
  activity TEXT NOT NULL,
  title TEXT NOT NULL,
  genre TEXT NOT NULL,
  rating INTEGER NOT NULL,
  comments TEXT
);

CREATE TABLE whibut_books (
  id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  user_id INTEGER REFERENCES whibut_users(id) ON DELETE CASCADE NOT NULL,
  activity TEXT NOT NULL,
  title TEXT NOT NULL,
  author TEXT NOT NULL,
  genre TEXT NOT NULL,
  rating INTEGER NOT NULL,
  comments TEXT
);

CREATE TABLE whibut_tv (
  id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  user_id INTEGER REFERENCES whibut_users(id) ON DELETE CASCADE NOT NULL,
  activity TEXT NOT NULL,
  title TEXT NOT NULL,
  network TEXT NOT NULL,
  genre TEXT NOT NULL,
  rating INTEGER NOT NULL,
  comments TEXT
);

CREATE TABLE whibut_restaurants (
  id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  user_id INTEGER REFERENCES whibut_users(id) ON DELETE CASCADE NOT NULL,
  activity TEXT NOT NULL,
  restaurant_name TEXT NOT NULL,
  restaurant_type TEXT NOT NULL,
  website TEXT,
  rating INTEGER NOT NULL,
  comments TEXT
);